public class switchstatement {
    public static void main(String[] args) {
        int n = 4;
        switch (n) {
            case 1:
                System.out.println("samosa");
                break;
            case 2:
                System.out.println("momos");
                break;

            case 3:
                System.out.println("burger");
                break;

            default:
                System.out.println("it's your nightmare");

        }
    }
}
