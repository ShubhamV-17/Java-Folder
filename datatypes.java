public class datatypes {
    public static void main(String[] args) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        float f = 10.5f;
        System.out.println(f);
        // long
        // double
        short n = 240;
        System.out.println(n);
    }
}
